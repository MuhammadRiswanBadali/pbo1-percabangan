public class percabanganSwitch {
    
    public static void main (String[] args){

        int bulan = 1 ;

        switch(bulan){
            case 1:
                System.out.println("bulan januari");
                break;
            case 2:
                System.out.println("bulan februari");
                break;
            default:
                System.out.println("bulan tidak ada");
        }
    }
}
